public class Reto5 {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        
        for (int numero = 1; numero <= 100; numero += 2) {
            System.out.println(numero);
        }
        
        for (int i = 1; i <= 100 ; i++) {
            System.out.println(i);
            i++;
        }
        
        
    }
    
}
